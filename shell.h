/**
 * struct liststr - singly linked list
 * @num: the numerical field
 * @cord: a string
 * @next: makes a reference to the next node
 */
typedef struct liststr
{
	int num;
	char *cord;
	struct liststr *next;
} list_val;

/**
 * struct passinfo - includes pseudo-arguments for use in functions,
 * allowing for a standardized prototype for the function pointer struct
 * @arg: a string containing arguments generated by getline
 * @argv: a string array derived from arg
 * @path: a route in string for the current command
 * @argc: the number of arguments
 * @line_count: the number of errors
 * @err_num: the error code for exit()s
 * @linecount_flag: if this line of input is counted
 * @fname: the programâ€™s file name
 * @env: local copy of the linked list environ
 * @environ: a customized copy of environ derived from LL env
 * @history: the node of history
 * @alias: the node of the alias
 * @env_changed: if the environ was altered
 * @status: the return status of the most recently executed command
 * @cmd_buf: if chaining is enabled, the location of the reference to cmd_buf
 * @cmd_buf_type: CMD_type ||, &&, ;
 * @readval: the val to read line input from
 * @histcount: the amount of historical lines
 */
typedef struct passinfo
{
	char *arg;
	char **argv;
	char *path;
	int argc;
	unsigned int line_count;
	int err_num;
	int linecount_flag;
	char *fname;
	list_val *env;
	list_val *history;
	list_val *alias;
	char **environ;
	int env_changed;
	int status;

	char **cmd_buf; /* pointer to cmd ; chain buffer, for memory mangement */
	int cmd_buf_type; /* CMD_type ||, &&, ; */
	int readval;
	int histcount;
} info_val;

